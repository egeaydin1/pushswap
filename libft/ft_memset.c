#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = (char *)s;
	while (n > 0)
	{
		ptr[i] = c;
		i++;
		n--;
	}
	return (s);
}
